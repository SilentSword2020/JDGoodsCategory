package com.study.app.jd.goods.category.ui.main.model;

/**
 * 排序
 */
public class SortItem {
    /**
     * 排序id
     */
    private String sId;

    /**
     * 名称
     */
    private String name;

    public void setSId(String sId) {
        this.sId = sId;
    }

    public String getSId() {
        return this.sId;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return this.name;
    }
}